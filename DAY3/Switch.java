public class Switch{
    public static void main(String[] args) throws Exception{
        
        String s = "hey tanu";
        switch(s){
        case "GOOD MORNING": 
            System.out.println("ZERO");
            break;
        case "gOOOD NIGHT": 
            System.out.println("3");
            break;

        case "HEY HEY": 
            System.out.println("4");
            break;
        default: 
            System.out.println("NOT FOUND");


        }
    }
}